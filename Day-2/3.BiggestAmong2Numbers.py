# 3. Biggest Among Two Numbers
# Question: Find the biggest number among two. Explanation: Use comparison operators (>) to check which number is greater. 
# - Input: A = 4, B = 7 - Output: Biggest is: 7

A = int(input("Enter A:"))
B = int(input("Enter B:"))
if A>B:
    print("largest value is:",A)
else:
    print("largest value is :",B)